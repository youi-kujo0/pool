int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				c;

	i = 0;
	while (i < n && s1[i] != 0 && s2[i] != 0)
	{
		c = s1[i] - s2[i];
		if (c != 0)
			return (c);
		i++;
	}
	if (i == n)
		return (0);
	return (s1[i] - s2[i]);
}
